
	SELECT
	    SUBSTR_AFTER("TBOUT".RESOURCE_OR_CENTER_GBO,',') AS "RESRCE",
		SUBSTR_BEFORE( SUBSTR_AFTER( "TBOUT".SFC_ROUTER_BO, ',' ), ',' ) AS "HLSFC"
	FROM
		SFC_STEP AS "TBOUT"
	WHERE
		"TBOUT".RESOURCE_OR_CENTER_GBO IN(
			'ResourceBO:2002,2EBZ0001',
			'ResourceBO:2002,2EBZ0002',
			'ResourceBO:2002,2EBZ0003',
			'ResourceBO:2002,2EBZ0004',
			'ResourceBO:2002,2EBZ0005',
			'ResourceBO:2002,2FBZ0006',
			'ResourceBO:2002,2FBZ0007',
			'ResourceBO:2002,2FBZ0008',
			'ResourceBO:2002,2FBZ0009'
		)
		AND "TBOUT".QTY_IN_QUEUE IS NOT NULL
		AND "TBOUT".QTY_IN_QUEUE != 0
		AND LEFT(
			"TBOUT".OPERATION_BO,
			23
		)= 'OperationBO:2002,C21070'
		AND "TBOUT".DATE_QUEUED =(
			SELECT
				MAX( "TBINNER".DATE_QUEUED )
			FROM
				SFC_STEP AS "TBINNER"
			WHERE
				"TBINNER".RESOURCE_OR_CENTER_GBO = "TBOUT".RESOURCE_OR_CENTER_GBO
		)